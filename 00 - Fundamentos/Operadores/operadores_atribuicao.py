# Define a variável 'saldo' com o valor 500
saldo = 500
print(saldo)  # Saída: 500

# Reatribui o valor da variável 'saldo' para 200
saldo = 200
print(saldo)  # Saída: 200

# Incrementa o valor da variável 'saldo' em 10
saldo += 10
print(saldo)  # Saída: 210

# Subtrai 5 do valor da variável 'saldo'
saldo -= 5
print(saldo)  # Saída: 205

# Realiza a divisão inteira da variável 'saldo' por 2
saldo //= 2
print(saldo)  # Saída: 102

# Realiza a divisão da variável 'saldo' por 2 (divisão de ponto flutuante)
saldo /= 2
print(saldo)  # Saída: 51.0

# Multiplica o valor da variável 'saldo' por 10
saldo *= 10
print(saldo)  # Saída: 510.0

# Calcula o resto da divisão da variável 'saldo' por 4
saldo %= 4
print(saldo)  # Saída: 2.0

# Calcula o quadrado da variável 'saldo
saldo **= 2
print(saldo)  # Saída: 4.0
